function printCardOption($number, $cardName, $star, $selected){
    echo '<option value="' . $number . '"';
    if($selected) echo ' selected="selected" ';
    echo '>No.';// . $number . ' - ' . $star . ' 星';// . $cardName , " ";// . '（'. $star .'星）</option>';
    if($number < 100 && $number >= 10)
        echo '0' . $number;
    elseif($number < 10)
        echo '00' . $number;
    else
        echo $number;
    echo ' - ' . $star . ' 星';
    for($i = 0; $i < $star; $i++){
        echo "★";
    }
    for($j = 0; $j < 7-$star; $j++){
        echo "☆";
    }
    echo ' ' . $cardName . '</option>';
}
